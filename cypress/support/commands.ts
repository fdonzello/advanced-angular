// -- This is a parent command --
Cypress.Commands.add('getX', (selector) => {
  return cy.get(`[data-cy="${selector}"]`)
})

Cypress.Commands.add('expectBalance', (expectedValue) => {
  return cy.get(`[data-cy="balance"]`).should('contain', expectedValue)
})

